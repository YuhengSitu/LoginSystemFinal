    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String codeName = jTextField1.getText();
        String[] codeNames;
        try {
            codeNames = loginSystem.loadCodeNames("dictionary.txt");
            int binaryResult = loginSystem.binarySearch(codeName, codeNames); 
            if(binaryResult == -1) {
                JOptionPane.showMessageDialog(this, "Code Name is available", "Success", JOptionPane.ERROR_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this, "Code Name is Unavailable", "Failed", JOptionPane.ERROR_MESSAGE);
            }     
        } catch (IOException ex) {
            Logger.getLogger(loginFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }//GEN-LAST:event_jButton1ActionPerformed

